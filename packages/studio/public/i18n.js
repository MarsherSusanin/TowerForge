const originalText = new WeakMap();
const originalAttributes = new WeakMap();
let applying = false;
let observer;

export function getLanguage() {
  try {
    const value = localStorage.getItem(STORAGE_KEY);
    return SUPPORTED.has(value) ? value : "ru";
  } catch {
    return "ru";
  }
}

export function t(source) {
  const value = String(source ?? "");
  if (getLanguage() === "en" || !value) return value;
  if (RU[value]) return RU[value];
  for (const [pattern, replacement] of RU_PATTERNS) {
    if (pattern.test(value)) return value.replace(pattern, replacement);
  }
  return value;
}

function shouldSkip(node) {
  const element = node.nodeType === Node.ELEMENT_NODE ? node : node.parentElement;
  if (!element) return true;
  if (element.closest("[data-i18n]")) return false;
  return Boolean(element.closest("script, style, textarea, [data-i18n-skip], .mono, #project-name, .item-name, .m-name, .wm-name, .tree-row-name, .pt-tname, .theme-pack-title"));
}

function localizeTextNode(node, refreshOriginal = false) {
  if (shouldSkip(node)) return;
  if (refreshOriginal || !originalText.has(node)) originalText.set(node, node.nodeValue);
  const source = originalText.get(node) ?? node.nodeValue;
  const trimmed = source.trim();
  if (!trimmed) return;
  const translated = t(trimmed);
  if (translated === trimmed && getLanguage() === "ru") return;
  const start = source.slice(0, source.indexOf(trimmed));
  const end = source.slice(source.indexOf(trimmed) + trimmed.length);
  node.nodeValue = `${start}${translated}${end}`;
}

function localizeAttributes(element, refreshOriginal = false) {
  if (shouldSkip(element)) return;
  let values = originalAttributes.get(element);
  if (!values) {
    values = new Map();
    originalAttributes.set(element, values);
  }
  for (const attribute of ["title", "aria-label", "placeholder", "alt"]) {
    if (!element.hasAttribute(attribute)) continue;
    if (refreshOriginal || !values.has(attribute)) values.set(attribute, element.getAttribute(attribute));
    element.setAttribute(attribute, t(values.get(attribute)));
  }
}

export function localizeTree(root = document.documentElement) {
  applying = true;
  try {
    if (root.nodeType === Node.TEXT_NODE) localizeTextNode(root);
    if (root.nodeType === Node.ELEMENT_NODE) localizeAttributes(root);
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
    let node;
    while ((node = walker.nextNode())) {
      if (node.nodeType === Node.TEXT_NODE) localizeTextNode(node);
      else localizeAttributes(node);
    }
    document.documentElement.lang = getLanguage();
    observer?.takeRecords();
  } finally {
    applying = false;
  }
}

export function setLanguage(language) {
  const next = SUPPORTED.has(language) ? language : "ru";
  try { localStorage.setItem(STORAGE_KEY, next); } catch { /* ignore */ }
  localizeTree();
  window.dispatchEvent(new CustomEvent("towerforge:languagechange", { detail: { language: next } }));
}

export function initI18n() {
  localizeTree();
  observer?.disconnect();
  observer = new MutationObserver((mutations) => {
    if (applying) return;
    applying = true;
    try {
      for (const mutation of mutations) {
        if (mutation.type === "characterData") localizeTextNode(mutation.target, true);
        if (mutation.type === "attributes") localizeAttributes(mutation.target, true);
        for (const node of mutation.addedNodes ?? []) localizeTree(node);
      }
      observer?.takeRecords();
    } finally {
      applying = false;
    }
  });
  observer.observe(document.documentElement, {
    subtree: true,
    childList: true,
    characterData: true,
    attributes: true,
    attributeFilter: ["title", "aria-label", "placeholder", "alt"],
  });
}

